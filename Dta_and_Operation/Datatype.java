
public class Datatype { //파일명과 똑같은 이름으로 하기
	
	//데이터 타입이 중요한 이유는 타입별로 연산의 방법이 각각 다르기 때문이다.
	
	public static void main(String[] args) {
		System.out.println(6); //Number
		System.out.println("six"); // String
		
		System.out.println("6"); // String 6
		
		System.out.println(6+6);
		System.out.println("6"+"6"); //문자열을 위한 결합연산자
		
		System.out.println("111".length()); //문자열에 대한 길이 반환
		
		
	}

}
